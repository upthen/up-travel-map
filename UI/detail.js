let currentLightboxImages = []
let currentLightboxIndex = 0
let currentRecordPhotos = []
let carouselIndex = 0
let galleryMode = 'carousel'
let autoPlayTimer = null

function initDetail() {
	const params = new URLSearchParams(window.location.search)
	const cityId = parseInt(params.get('city'))

	if (!cityId || !cities.find((c) => c.id === cityId)) {
		window.location.href = 'index.html'
		return
	}

	const city = cities.find((c) => c.id === cityId)
	const records = cityRecords[cityId] || []

	renderHero(city)
	renderRecords(records)
	setupLightbox()
	setupGalleryModeToggle()
}

function renderHero(city) {
	document.getElementById('hero-bg-image').src = city.photo
	document.getElementById('hero-bg-image').alt = city.name
	document.getElementById('hero-seal').textContent = city.name.charAt(0)
	document.getElementById('hero-city-name').textContent = city.name
	document.getElementById('hero-time').textContent = city.visitTime
	document.getElementById('hero-companions').textContent = city.companions
	document.getElementById('hero-photo-count').textContent = city.photoCount
	document.getElementById('hero-days').textContent = city.days
	document.getElementById('hero-description').textContent = city.description
	document.title = `${city.name} · 足迹中国`
}

function renderRecords(records) {
	const container = document.getElementById('records-container')
	const recordsSection = document.getElementById('records-section')
	const detailSection = document.getElementById('record-detail-section')

	if (records.length === 0) {
		recordsSection.style.display = 'none'
		return
	}

	if (records.length === 1) {
		recordsSection.style.display = 'none'
		showRecordDetail(records[0])
		return
	}

	recordsSection.style.display = ''
	container.innerHTML = ''

	records.forEach((record, index) => {
		const card = document.createElement('div')
		card.className = 'record-card'
		card.style.animationDelay = `${index * 0.1}s`

		const coverPhoto = record.photos[0] || ''
		const paragraphs = record.text.split('\n').filter((p) => p.trim())
		const excerpt = paragraphs[0] || ''

		card.innerHTML = `
			<div class="record-card-badge">${record.photos.length} 张</div>
			<div class="record-card-cover">
				<img src="${coverPhoto}" alt="${record.title}" loading="lazy" />
				<div class="record-card-cover-overlay"></div>
			</div>
			<div class="record-card-body">
				<div class="record-card-title">${record.title}</div>
				<div class="record-card-meta">
					<span>${record.date}</span>
					<span class="meta-dot">·</span>
					<span>${record.photos.length} 张照片</span>
				</div>
				<div class="record-card-excerpt">${excerpt}</div>
			</div>
		`

		card.addEventListener('click', () => {
			showRecordDetail(record)
		})

		container.appendChild(card)
	})
}

function showRecordDetail(record) {
	const recordsSection = document.getElementById('records-section')
	const detailSection = document.getElementById('record-detail-section')

	recordsSection.style.display = 'none'
	detailSection.style.display = ''

	document.getElementById('record-detail-title').textContent = record.title
	document.getElementById('record-detail-date').textContent = record.date
	document.getElementById('record-detail-photos-count').textContent =
		record.photos.length

	currentRecordPhotos = record.photos
	renderGallery(record.photos)
	renderCarousel(record.photos)
	applyGalleryMode()
	renderText(record.text)

	detailSection.scrollIntoView({ behavior: 'smooth', block: 'start' })
}

function renderGallery(photos) {
	const gallery = document.getElementById('record-gallery')
	gallery.innerHTML = ''

	photos.forEach((photo, index) => {
		const item = document.createElement('div')
		item.className = 'record-gallery-item'
		item.innerHTML = `<img src="${photo}" alt="照片 ${index + 1}" loading="lazy" />`

		item.addEventListener('click', () => {
			openLightbox(photos, index)
		})

		gallery.appendChild(item)
	})
}

function renderCarousel(photos) {
	const track = document.getElementById('carousel-track')
	const dots = document.getElementById('carousel-dots')
	track.innerHTML = ''
	dots.innerHTML = ''

	photos.forEach((photo, index) => {
		const slide = document.createElement('div')
		slide.className = 'carousel-slide'
		slide.innerHTML = `<img src="${photo}" alt="照片 ${index + 1}" loading="lazy" />`
		slide.addEventListener('click', () => {
			openLightbox(photos, index)
		})
		track.appendChild(slide)

		const dot = document.createElement('div')
		dot.className = 'carousel-dot' + (index === 0 ? ' active' : '')
		dot.addEventListener('click', () => {
			goToSlide(index)
		})
		dots.appendChild(dot)
	})

	carouselIndex = 0
	document.getElementById('carousel-total').textContent = photos.length
	document.getElementById('carousel-current').textContent = 1
	updateCarouselPosition()
}

function goToSlide(index) {
	carouselIndex = index
	if (carouselIndex < 0) carouselIndex = currentRecordPhotos.length - 1
	if (carouselIndex >= currentRecordPhotos.length) carouselIndex = 0
	updateCarouselPosition()
}

function updateCarouselPosition() {
	const track = document.getElementById('carousel-track')
	if (!track) return
	track.style.transform = `translateX(-${carouselIndex * 100}%)`

	const dots = document.querySelectorAll('.carousel-dot')
	dots.forEach((dot, i) => {
		dot.classList.toggle('active', i === carouselIndex)
	})

	document.getElementById('carousel-current').textContent = carouselIndex + 1
}

function startAutoPlay() {
	stopAutoPlay()
	autoPlayTimer = setInterval(() => {
		goToSlide(carouselIndex + 1)
	}, 4000)
}

function stopAutoPlay() {
	if (autoPlayTimer) {
		clearInterval(autoPlayTimer)
		autoPlayTimer = null
	}
}

function setupGalleryModeToggle() {
	const carouselEl = document.getElementById('gallery-carousel')
	const gridEl = document.getElementById('record-gallery')
	const btnCarousel = document.getElementById('mode-carousel')
	const btnGrid = document.getElementById('mode-grid')

	carouselEl.style.display = 'none'
	gridEl.style.display = ''

	btnCarousel.addEventListener('click', () => {
		galleryMode = 'carousel'
		btnCarousel.classList.add('active')
		btnGrid.classList.remove('active')
		applyGalleryMode()
	})

	btnGrid.addEventListener('click', () => {
		galleryMode = 'grid'
		btnGrid.classList.add('active')
		btnCarousel.classList.remove('active')
		applyGalleryMode()
	})

	document.getElementById('carousel-prev').addEventListener('click', () => {
		stopAutoPlay()
		goToSlide(carouselIndex - 1)
	})

	document.getElementById('carousel-next').addEventListener('click', () => {
		stopAutoPlay()
		goToSlide(carouselIndex + 1)
	})

	let touchStartX = 0
	const viewport = document.querySelector('.carousel-viewport')
	if (viewport) {
		viewport.addEventListener(
			'touchstart',
			(e) => {
				touchStartX = e.touches[0].clientX
				stopAutoPlay()
			},
			{ passive: true }
		)

		viewport.addEventListener(
			'touchend',
			(e) => {
				const diff = touchStartX - e.changedTouches[0].clientX
				if (Math.abs(diff) > 50) {
					goToSlide(carouselIndex + (diff > 0 ? 1 : -1))
				}
			},
			{ passive: true }
		)
	}
}

function applyGalleryMode() {
	const carouselEl = document.getElementById('gallery-carousel')
	const gridEl = document.getElementById('record-gallery')

	if (galleryMode === 'carousel') {
		carouselEl.style.display = ''
		gridEl.style.display = 'none'
		goToSlide(0)
		startAutoPlay()
	} else {
		carouselEl.style.display = 'none'
		gridEl.style.display = ''
		stopAutoPlay()
	}
}

function renderText(text) {
	const container = document.getElementById('record-text')
	const paragraphs = text.split('\n').filter((p) => p.trim())

	container.innerHTML = `
		<div class="record-text-title">旅行笔记</div>
		<div class="record-text-content">
			${paragraphs.map((p) => `<p>${p}</p>`).join('')}
		</div>
	`
}

function setupLightbox() {
	const overlay = document.createElement('div')
	overlay.className = 'lightbox-overlay'
	overlay.id = 'lightbox'
	overlay.innerHTML = `
		<button class="lightbox-close" id="lightbox-close">
			<svg viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2">
				<line x1="18" y1="6" x2="6" y2="18" />
				<line x1="6" y1="6" x2="18" y2="18" />
			</svg>
		</button>
		<button class="lightbox-nav lightbox-prev" id="lightbox-prev">
			<svg viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2">
				<polyline points="15 18 9 12 15 6" />
			</svg>
		</button>
		<img class="lightbox-image" id="lightbox-image" src="" alt="放大照片" />
		<button class="lightbox-nav lightbox-next" id="lightbox-next">
			<svg viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2">
				<polyline points="9 18 15 12 9 6" />
			</svg>
		</button>
	`
	document.body.appendChild(overlay)

	document
		.getElementById('lightbox-close')
		.addEventListener('click', closeLightbox)
	document.getElementById('lightbox-prev').addEventListener('click', () => {
		navigateLightbox(-1)
	})
	document.getElementById('lightbox-next').addEventListener('click', () => {
		navigateLightbox(1)
	})
	overlay.addEventListener('click', (e) => {
		if (e.target === overlay) closeLightbox()
	})

	document.addEventListener('keydown', (e) => {
		if (!document.getElementById('lightbox').classList.contains('open')) return
		if (e.key === 'Escape') closeLightbox()
		if (e.key === 'ArrowLeft') navigateLightbox(-1)
		if (e.key === 'ArrowRight') navigateLightbox(1)
	})
}

function openLightbox(images, index) {
	currentLightboxImages = images
	currentLightboxIndex = index
	document.getElementById('lightbox-image').src = images[index]
	document.getElementById('lightbox').classList.add('open')
	document.body.style.overflow = 'hidden'
}

function closeLightbox() {
	document.getElementById('lightbox').classList.remove('open')
	document.body.style.overflow = ''
}

function navigateLightbox(direction) {
	currentLightboxIndex += direction
	if (currentLightboxIndex < 0)
		currentLightboxIndex = currentLightboxImages.length - 1
	if (currentLightboxIndex >= currentLightboxImages.length)
		currentLightboxIndex = 0
	document.getElementById('lightbox-image').src =
		currentLightboxImages[currentLightboxIndex]
}

